// Registration returns the core.WorkloadReg for the "app" kind. Call this from the composition
// root's catalog wiring file; no init() self-registration.
func Registration() core.WorkloadReg {
	return core.WorkloadReg{
		Kind:         kind,
		Doc:          "app — blueprint-declared service GRAPH; each node emits custom (DSL) metrics/logs/spans, one correlated trace across the graph",
		NewConfig:    NewConfig,
		Build:        build,
		FailureModes: FailureModes,
	}
}

// Workload is one app instance: a resolved service graph bound to a cluster/env.
type Workload struct {
	cfg Config
	b   core.Binding

	// resolved identity, frozen at Build from the binding.
	cluster string
	env     string
	weight  float64
	nonProd bool

	graph *graph       // validated graph (nodes + edges + effective specs)
	m     *minter      // entry-node invocation minter
	st    *state.State // cumulative metric state
}

// build constructs a Workload: decode-time defaults, resolve + validate the graph, freeze identity.
func build(cfgAny any, b core.Binding) (core.Workload, error) {
	cfg, _ := cfgAny.(*Config)
	if cfg == nil {
		cfg = NewConfig().(*Config)
	}
	if cfg.Traffic.PeakRPS <= 0 {
		cfg.Traffic.PeakRPS = 50
	}
	if cfg.Traffic.OffPeakRPS < 0 {
		cfg.Traffic.OffPeakRPS = 0
	}
	g, err := buildGraph(cfg.Services)
	if err != nil {
		return nil, err
	}
	w := &Workload{cfg: *cfg, b: b, graph: g, st: state.NewState()}
	w.resolveIdentity()
	// Wire each db/cache leaf that declared db_instance to its env's RDS/cache fixture (the resolver
	// owns the per-env identity; the workload only reads it from the binding — no minting).
	if err := g.resolveDBIdentities(w.env, b.Databases); err != nil {
		return nil, err
	}
	w.m = newMinter(w.b.Name, w.env, w.cluster, w.weight, w.nonProd, cfg.Traffic, cfg.Models, g)
	// Wire BrowserOrigin minting: the minter sets r.BrowserOrigin only when RUM is active
	// (entry has rum_faro + binding carries a Faro sink). Mirror webservice minter's cfg.RUM flag.
	w.m.rumEnabled = w.rumEnabled()
	return w, nil
}

// resolveIdentity pins cluster/env/weight from the binding once. Absent dimensions are OMITTED
// downstream — never sentinelled (I13).
func (w *Workload) resolveIdentity() {
	if w.b.Env != nil {
		w.env = w.b.Env.Name
		w.weight = w.b.Env.Weight
		w.nonProd = w.b.Env.NonProd
	}
	if w.weight == 0 {
		w.weight = 1.0
	}
	if w.b.Cluster != nil {
		w.cluster = w.b.Cluster.Name
	}
}

// Kind implements core.Workload.
func (w *Workload) Kind() string { return kind }

// Name implements core.Workload.
func (w *Workload) Name() string { return w.b.Name }

// Signals declares the classes this instance emits (Metrics + Traces + Logs; RUM is appended
// when rumEnabled; PyroscopeProfiles is appended when at least one node declares pyroscope in
// sdk mode — scraped mode is handled by Alloy, no push sink).
func (w *Workload) Signals() []core.SignalClass {
	sigs := []core.SignalClass{core.Metrics, core.Traces, core.Logs}
	if w.rumEnabled() {
		sigs = append(sigs, core.RUM)
	}
	for _, svc := range w.cfg.Services {
		if svc.Pyroscope != nil && svc.Pyroscope.Enabled && svc.Pyroscope.ModeOrDefault() != "scraped" {
			sigs = append(sigs, core.PyroscopeProfiles)
			break
		}
	}
	return sigs
}

// rumEnabled reports whether the Faro/RUM beacon lane is active for this workload. True when:
//   - the binding carries a Faro sink (b.RUM != nil), AND
//   - the entry node is a frontend type carrying the rum_faro catalog profile.
//
// Mirrors webservice.rumEnabled (cfg.RUM && b.RUM != nil) adapted to the app graph:
// instead of a config flag, the rum_faro profile on the entry node is the emission switch.
func (w *Workload) rumEnabled() bool {
	if w.b.RUM == nil {
		return false
	}
	return w.graph.entryHasRumFaro()
}

// Interval implements core.Workload (metric lane cadence).
func (w *Workload) Interval() time.Duration { return interval }

// Minter implements core.Workload.
func (w *Workload) Minter() ledger.Minter { return w.m }
